    private void loadAPIKeyFromEnv(String envKey, java.util.function.Consumer<String> setter) {
        try {
            String envValue = System.getenv(envKey);
            if (envValue != null && !envValue.trim().isEmpty()) {
                setter.accept(envValue.trim());
                System.out.println("‚úÖ " + envKey + " loaded from environment");
            }
        } catch (Exception e) {
            System.err.println("‚ùå Failed to load " + envKey + " from environment: " + e.getMessage());
        }
    }
    
