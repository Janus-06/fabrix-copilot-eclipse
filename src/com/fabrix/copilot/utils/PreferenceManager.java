    public boolean validateSettings() {
        boolean isValid = true;
        
        try {
            if (!hasValidAPIKey()) {
                System.out.println("‚ö†Ô∏è No API keys configured");
                isValid = false;
            }
            
            if (isMCPEnabled()) {
                if (getMCPServerUrl().isEmpty() || getMCPPort().isEmpty()) {
                    System.out.println("‚ö†Ô∏è MCP enabled but incomplete configuration");
                    isValid = false;
                }
                
                try {
                    Integer.parseInt(getMCPPort());
                } catch (NumberFormatException e) {
                    System.out.println("‚ö†Ô∏è Invalid MCP port number");
                    isValid = false;
                }
            }
        } catch (Exception e) {
            System.err.println("ÏÑ§Ï†ï Í≤ÄÏ¶ù Ïã§Ìå®: " + e.getMessage());
            isValid = false;
        }
        
        return isValid;
    }
    
